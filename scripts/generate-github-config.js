import fs from 'fs';
import path from 'path';
import yaml from 'js-yaml';

function generateGitHubConfig() {
  // æ˜¾ç¤ºå¸®åŠ©ä¿¡æ¯
  if (process.argv.includes('--help') || process.argv.includes('-h')) {
    console.log(`
Usage: node generate-github-config.js [options]

Automatic file detection:
  - Checks parent directory first: ../yaal.config.yaml
  - Falls back to local directory: ./yaal.config.yaml

Options:
  --config=<path>    Path to configuration file (optional, overrides automatic detection)
  --readme=<path>    Path to README file (optional, overrides automatic detection)
  --help, -h         Show this help message

Examples:
  node generate-github-config.js                    # Automatic detection
  node generate-github-config.js --config=./config.yaml
  node generate-github-config.js --config=./config.yaml --readme=./docs/README.md
`);
    return;
  }

  try {
    // è§£æå‘½ä»¤è¡Œå‚æ•°
    const args = process.argv.slice(2);
    let configPath = null;
    let readmePath = null;

    // è§£æå‘½åå‚æ•°ï¼ˆä¿ç•™å‘åå…¼å®¹ï¼‰
    for (let i = 0; i < args.length; i++) {
      const arg = args[i];
      if (arg.startsWith('--config=')) {
        configPath = path.resolve(arg.split('=')[1]);
      } else if (arg.startsWith('--readme=')) {
        readmePath = path.resolve(arg.split('=')[1]);
      }
    }

    // è‡ªåŠ¨æŸ¥æ‰¾é…ç½®æ–‡ä»¶ï¼šå…ˆæ£€æŸ¥çˆ¶ç›®å½•ï¼Œå†æ£€æŸ¥æœ¬åœ°ç›®å½•
    if (!configPath) {
      const parentConfigPath = path.resolve(
        process.cwd(),
        '../yaal.config.yaml'
      );
      const localConfigPath = path.resolve(process.cwd(), 'yaal.config.yaml');

      if (fs.existsSync(parentConfigPath)) {
        configPath = parentConfigPath;
        console.log(`ğŸ“ Using parent directory config: ${configPath}`);
      } else if (fs.existsSync(localConfigPath)) {
        configPath = localConfigPath;
        console.log(`ğŸ“ Using local directory config: ${configPath}`);
      } else {
        console.error(
          `âŒ yaal.config.yaml not found in parent or local directory. Please create this file with your GitHub repository URL.`
        );
        process.exit(1);
      }
    }

    // è‡ªåŠ¨æŸ¥æ‰¾READMEæ–‡ä»¶ï¼šå…ˆæ£€æŸ¥çˆ¶ç›®å½•ï¼Œå†æ£€æŸ¥æœ¬åœ°ç›®å½•
    if (!readmePath) {
      const parentReadmePath = path.resolve(process.cwd(), '../README.md');
      const localReadmePath = path.resolve(process.cwd(), 'README.md');

      if (fs.existsSync(parentReadmePath)) {
        readmePath = parentReadmePath;
        console.log(`ğŸ“ Using parent directory README: ${readmePath}`);
      } else if (fs.existsSync(localReadmePath)) {
        readmePath = localReadmePath;
        console.log(`ğŸ“ Using local directory README: ${readmePath}`);
      } else {
        console.error(
          `âŒ README.md not found in parent or local directory. Please create this file.`
        );
        process.exit(1);
      }
    }

    const configContent = fs.readFileSync(configPath, 'utf8');
    const config = yaml.load(configContent) || {};

    if (!config.github) {
      console.error(
        'âŒ GitHub URL not found in yaal.config.yaml. Please add "github: https://github.com/owner/repo"'
      );
      process.exit(1);
    }

    const url = config.github;
    const match = url.match(/github\.com\/([^\/]+)\/([^\/]+)/);

    if (!match) {
      console.error(
        `âŒ Invalid GitHub URL format: ${url}. Expected: https://github.com/owner/repo`
      );
      process.exit(1);
    }

    const owner = match[1];
    const repo = match[2].replace(/\.git$/, '');

    // ä» README è§£ææ ‡é¢˜
    let title = repo;
    try {
      if (fs.existsSync(readmePath)) {
        const readmeContent = fs.readFileSync(readmePath, 'utf8');
        const titleMatch = readmeContent.match(/^#\s+(.+)$/m);
        if (titleMatch) {
          title = titleMatch[1].trim();
        }
      } else {
        console.warn(
          `âš ï¸ README.md not found at ${readmePath}, using repo name as title`
        );
      }
    } catch (error) {
      console.warn(
        `âš ï¸ Failed to read README.md: ${error.message}, using repo name as title`
      );
    }

    const outputContent = `// This file is auto-generated by scripts/generate-github-config.js
// Do not edit this file directly. Edit yaal.config.yaml instead.

export const githubConfig = {
  url: '${url}',
  owner: '${owner}',
  repo: '${repo}',
  title: '${title}',
  description: 'A meta awesome list of awesome lists'
};

export const siteConfig = {
  title: '${title}',
  description: 'A meta awesome list of awesome lists',
  url: 'https://${owner}.github.io/${repo}/'
};
`;

    fs.writeFileSync(
      path.join(process.cwd(), 'lib', 'githubConfig.ts'),
      outputContent
    );

    console.log(`âœ… GitHub configuration generated:
   Repository: ${owner}/${repo}
   URL: ${url}`);
  } catch (error) {
    console.error(
      `âŒ Failed to generate GitHub configuration: ${error.message}`
    );
    process.exit(1);
  }
}

generateGitHubConfig();
